package com.virugan.entry;

public class myFileEntry {

    private String fileName;

    private String filePath;

    private String fileSize;

    private String fileSuff;

    private String opeiTime;

    private Integer fileNumb;

    public Integer getFileNumb() {
        return fileNumb;
    }

    public void setFileNumb(Integer fileNumb) {
        this.fileNumb = fileNumb;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getFileSize() {
        return fileSize;
    }

    public void setFileSize(String fileSize) {
        this.fileSize = fileSize;
    }

    public String getFileSuff() {
        return fileSuff;
    }

    public void setFileSuff(String fileSuff) {
        this.fileSuff = fileSuff;
    }

    public String getOpeiTime() {
        return opeiTime;
    }

    public void setOpeiTime(String opeiTime) {
        this.opeiTime = opeiTime;
    }


}
